    public void showDoctorDetails(String id) {
        getContentPane().removeAll();
        add(new DoctorProfilePanel(id));
        setLayout(new FlowLayout());
        repaint();
        revalidate();
    }
    
    private int getRowsForPatients() {
        int size = DoctorDirectory.getDoctorList().size();
        if (size%2 == 0) return size/2;
        else return (size+1)/2;
    }
    
